    <!-- Section 1 : Les agents économiques -->
    <section class="mb-5 p-4 rounded shadow-sm box-element">
      <h2 class="h3 mb-4 text-success">1. Qui sont les acteurs de l’économie ?</h2>
      <p>Une entreprise ne vit pas seule : elle échange avec plusieurs <strong>agents économiques</strong>, c’est-à-dire des groupes qui jouent un rôle précis dans l’économie.</p>
      <ul class="list-group list-group-flush box-element">
        <li class="list-group-item"><strong>Les ménages</strong> : ils consomment (achètent) des biens et services, et vendent leur travail aux entreprises.</li>
        <li class="list-group-item"><strong>Les entreprises</strong> : elles produisent des biens ou services <em>marchands</em> (vendus à un prix couvrant les coûts).</li>
        <li class="list-group-item"><strong>Les banques</strong> : elles collectent l’épargne et prêtent de l’argent (crédits) pour financer l’économie.</li>
        <li class="list-group-item"><strong>L’État et les administrations publiques</strong> : elles produisent des services <em>non marchands</em> (ex. : école, hôpital, police), souvent gratuits ou à prix très bas.</li>
      </ul>
    </section>

    <!-- Section 2 : Le marché et le prix -->
    <section class="mb-5 p-4 rounded shadow-sm box-element">
      <h2 class="h3 mb-4 text-success">2. Comment fonctionne un marché ?</h2>
      <p>Un <strong>marché</strong> est un lieu (physique ou virtuel) où se rencontrent <strong>l’offre</strong> (ce que les entreprises vendent) et <strong>la demande</strong> (ce que les consommateurs veulent acheter).</p>

      <h4 class="mt-3">Le prix d’équilibre</h4>
      <p>C’est le prix où <strong>toute l’offre est vendue</strong> et <strong>toute la demande est satisfaite</strong>. Il se forme naturellement :</p>
      <ul>
        <li>Si le prix est <strong>trop haut</strong> → moins de gens achètent → la demande baisse.</li>
        <li>Si le prix est <strong>trop bas</strong> → les entreprises gagnent peu → l’offre diminue.</li>
      </ul>
      <p>Le prix joue donc le rôle de <strong>signal</strong> : il guide les choix des acheteurs et des vendeurs.</p>
    </section>

    <!-- Section 3 : La concurrence idéale -->
    <section class="mb-5 p-4 rounded shadow-sm box-element">
      <h2 class="h3 mb-4 text-success">3. Qu’est-ce qu’un marché concurrentiel ?</h2>
      <p>En théorie, un marché fonctionne bien quand :</p>
      <ul>
        <li>- <strong>Information parfaite</strong> : tout le monde connaît les prix, la qualité, etc.</li>
        <li>- <strong>Atomicité</strong> : il y a tellement d’acheteurs et de vendeurs qu’aucun ne peut influencer le prix seul.</li>
        <li>- <strong>Pas de barrières à l’entrée ou à la sortie</strong> : n’importe qui peut entrer ou quitter le marché facilement.</li>
      </ul>
      <p>En réalité, ces conditions ne sont <strong>jamais parfaitement remplies</strong>.</p>
    </section>

    <!-- Section 4 : Les dysfonctionnements du marché -->
    <section class="mb-5 p-4 rounded shadow-sm box-element">
      <h2 class="h3 mb-4 text-success">4. Quand le marché ne marche pas bien</h2>

      <h4>Barrières à l’entrée</h4>
      <p>Ce sont des obstacles qui empêchent de nouvelles entreprises d’entrer sur un marché :</p>
      <ul>
        <li><strong>Naturelles</strong> : coûts élevés, réglementation, technologie complexe.</li>
        <li><strong>Artificielles</strong> : stratégies des grandes entreprises (ex. : baisser les prix pour écraser la concurrence).</li>
      </ul>

      <h4>Asymétrie d’information</h4>
      <p>Certains acteurs ont plus d’infos que d’autres (ex. : un vendeur qui cache un défaut). Cela fausse les décisions.</p>

      <h4>Externalités</h4>
      <p>Ce sont des effets <strong>non payés</strong> d’une activité économique sur des tiers :</p>
      <ul>
        <li>- <strong>Positives</strong> : ex. un apiculteur aide les agriculteurs voisins (meilleure pollinisation).</li>
        <li>- <strong>Négatives</strong> : ex. une usine pollue l’air ou l’eau sans payer pour les dégâts.</li>
      </ul>
      <p>Dans ces cas, <strong>l’État doit intervenir</strong> (lois, taxes, subventions) pour corriger les déséquilibres.</p>
    </section>

